    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int idToRemove = Integer.parseInt(txtIDToRemove.getText());
        
        if(employees.get(idToRemove) instanceof Manager)
        {
            JOptionPane.showMessageDialog(this, "Cannot Remove The Manager", "ERROR", HEIGHT);
        }
        else if(employees.containsKey(idToRemove))
        {
            hotel.removeEmployee(idToRemove);
            txtIDToRemove.setText("");
            JOptionPane.showMessageDialog(this, "Employee Removed", "SUCCESS", HEIGHT);
            updateCSV();
        }
        else
        {
            JOptionPane.showMessageDialog(this, "ID does not exist", "ERROR", HEIGHT);
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

