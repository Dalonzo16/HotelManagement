    private void btnSearchIDToEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchIDToEditActionPerformed
        int idToEdit = Integer.parseInt(txtIDToEdit.getText());
        if(employees.containsKey(idToEdit))
        {
            Employee employeeToEdit = employees.get(idToEdit);
            txtEditFirstname.setText(employeeToEdit.getFirstName());
            txtEditLastname.setText(employeeToEdit.getLastName());
            txtEditEmail.setText(employeeToEdit.getEmail());
            txtEditPassword.setText(employeeToEdit.getPassword());
            txtEditShiftDuration.setText(Double.toString(employeeToEdit.getShift()));
            txtEditPayrate.setText(Double.toString(employeeToEdit.getPayRate()));
            txtEditFirstname.setVisible(true);
            txtEditLastname.setVisible(true);
            txtEditPassword.setVisible(true);
            txtEditPayrate.setVisible(true);
            txtEditShiftDuration.setVisible(true);
            txtEditEmail.setVisible(true);
            lblEditFirstname.setVisible(true);
            lblEditLastname.setVisible(true);
            lblEditPassword.setVisible(true);
            lblEditPayrate.setVisible(true);
            lblEditShiftDuration.setVisible(true);
            lblEditEmail.setVisible(true);
            btnEdit.setVisible(true);
            btnSearchIDToEdit.setVisible(false);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "ID does not exist", "ERROR", HEIGHT);
        }
    }//GEN-LAST:event_btnSearchIDToEditActionPerformed

