        txtEditPassword.setText("");
        txtEditShiftDuration.setText("");
        txtEditPayrate.setText("");
        JOptionPane.showMessageDialog(this, "Employee Information Updated.", "SUCCESS", HEIGHT);
        
        updateCSV();
