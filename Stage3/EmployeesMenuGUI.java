    private void readCSV()
    {
        try
        {
        File file = new File(filename);
        FileReader fr = new FileReader(file);
        BufferedReader br = new BufferedReader(fr);
        
        String line = "";
        String[] tempArr;
        while((line = br.readLine()) != null)
        {
            tempArr = line.split(delimiter);
            for(String field : tempArr)
            {
                System.out.println(field + "");
            }
            String firstname = tempArr[0];
            String lastname = tempArr[1];
            String password = tempArr[2];
            Double payrate = Double.valueOf(tempArr[3]);
            
            Employee employee = new Employee(firstname, lastname, password, payrate);
            hotel.addEmployee(employee);
        }
        br.close();
        }
        catch(IOException e)
        {
            JOptionPane.showMessageDialog(this, "Error reading the file.", "ERROR", HEIGHT);
        }
    }
    private void updateCSV()
    {
        try
        {
            File file = new File(filename);
            BufferedWriter bf = new BufferedWriter(new FileWriter(file));
            
            for(Map.Entry employee : employees.entrySet())
            {
                String row = "";
                int employeeID = (Integer)employee.getKey();
                Employee emp = employees.get(employeeID);
                
                String firstname = emp.getFirstName();
                String lastname = emp.getLastName();
                String password = emp.getPassword();
                double payrate = emp.getPayRate();
                
                row = firstname + "," + lastname + "," + password + "," + String.valueOf(payrate) + "\n";
                bf.write(row); 
            }
            bf.close();
        }
        catch(IOException e)
        {
            JOptionPane.showMessageDialog(this, "Error reading the file.", "ERROR", HEIGHT);
        }
    }

