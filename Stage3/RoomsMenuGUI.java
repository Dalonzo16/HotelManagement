    public DefaultTableModel populateAvailableRoomsTable(){
        DefaultTableModel model = (DefaultTableModel)availableRoomsTable.getModel();

        for(int key : hotel.getRooms().keySet()){
            Room room = hotel.getRooms().get(key);
            model.addRow(new String[]{String.valueOf(room.getRoomNumber()),String.valueOf(room.getFloorNumber()), "$" + String.format("%.2f",room.getPricePerNight()) + " per night"});
        }
        availableRoomsTable.setModel(model);
        return model;
    }
    public DefaultTableModel populateReservedRoomsTable(){
        DefaultTableModel model = (DefaultTableModel)reservedRoomsTable.getModel();

        for(int key : hotel.getReservedRooms().keySet()){
            Room room = hotel.getReservedRooms().get(key);
            model.addRow(new String[]{String.valueOf(room.getRoomNumber()),String.valueOf(room.getFloorNumber()), "$" + String.format("%.2f",room.getPricePerNight()) + " per night", });
        }
        reservedRoomsTable.setModel(model);
        return model;
    }
