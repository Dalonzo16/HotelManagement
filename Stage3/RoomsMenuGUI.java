    private void searchRoomInfoBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchRoomInfoBtnActionPerformed
        int roomNum  = Integer.parseInt(roomNumberTxt.getText());
        Room room = hotel.getRooms().get(roomNum);
        if(room == null){
            room = hotel.getReservedRooms().get(roomNum);
        }
        String row2 = "|Room Number  |   Floor Number    |   Price Per Night     |   Available?|\n";
        String row = String.valueOf( "|" + room.getRoomNumber()) + "                        |         " + String.valueOf(room.getFloorNumber()) + "                     |           " +
        String.format("%.2f",room.getPricePerNight()) + "             |     " + room.isAvailable() + "   |";
        roomInfoTxt.setText(row2 + row);
    }//GEN-LAST:event_searchRoomInfoBtnActionPerformed

